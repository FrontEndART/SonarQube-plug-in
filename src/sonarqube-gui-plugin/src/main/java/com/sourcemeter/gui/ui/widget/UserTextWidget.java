package com.sourcemeter.gui.ui.widget;

import org.sonar.api.web.AbstractRubyTemplate;
import org.sonar.api.web.Description;
import org.sonar.api.web.RubyRailsWidget;
import org.sonar.api.web.UserRole;
import org.sonar.api.web.WidgetCategory;
import org.sonar.api.web.WidgetProperties;
import org.sonar.api.web.WidgetProperty;
import org.sonar.api.web.WidgetPropertyType;

/**
 * There are no userText widget in SonarQube 4.0 anymore, so we have to implement our own one.
 */
@UserRole(UserRole.USER)
@Description("Displays the configured text. Intended to enable dashboard description.")
@WidgetCategory("SourceMeter")
@WidgetProperties({
        @WidgetProperty(
                key = "Title",
                type = WidgetPropertyType.STRING
        ),
        @WidgetProperty(
                key = "Text",
                type = WidgetPropertyType.TEXT
        )
})
public class UserTextWidget extends AbstractRubyTemplate implements RubyRailsWidget {

    @Override
    public String getId() {
        return "UserTextDisplayWidget";
    }

    @Override
    public String getTitle() {
        return "User Text Display";
    }

    @Override
    protected String getTemplatePath() {
        return "/widgets/sourcemeter-usertext-widget.html.erb";
    }
}
